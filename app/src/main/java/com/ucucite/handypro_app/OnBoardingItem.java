package com.ucucite.handypro_app;

public class OnBoardingItem {
    private int image;
    private String text;

    public OnBoardingItem(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
