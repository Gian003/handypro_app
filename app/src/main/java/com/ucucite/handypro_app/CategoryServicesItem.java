package com.ucucite.handypro_app;

public class CategoryServicesItem {
    private int image;
    private String label;

    public CategoryServicesItem(int image, String label) {
        this.image = image;
        this.label = label;
    }

    public int getImage() {
        return image;
    }

    public String getLabel() {
        return label;
    }
}
